  margin: 1.5rem 0;
}
.label {
  font-size: 1.25rem;
  font-weight: bold;
  margin-bottom: 0.5rem;
  line-height: 1em;
}
.hashtag {
  font-size: 1rem;
  margin-bottom: 0.25rem;
}
</style>
